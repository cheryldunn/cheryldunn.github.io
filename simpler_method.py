def give_rev(revenue):
    print("Revenues = " + revenue)
def give_exp(expense):
    print("Expenses = " + expenses)
def show_ni(net_income):
    print("Net Income = " + net_income)

revenue = [9999999, 888888, 77777, 6666]
expense = 55555

net_income = revenue - expense

for rev in revenues:
    give_rev(revenue)
    give_exp(expense)
    show_ni(net_income)
    
